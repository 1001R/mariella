package org.mariella.rcp.resources;

public class VResourceSaveException extends Exception {
	private static final long serialVersionUID = 1L;

public VResourceSaveException() {
}

public VResourceSaveException(String message) {
	super(message);
}

public VResourceSaveException(Throwable cause) {
	super(cause);
}

public VResourceSaveException(String message, Throwable cause) {
	super(message, cause);
}

}
